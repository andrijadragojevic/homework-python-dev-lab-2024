# Difference of squares solver

print("\nEnter the value of two squares you want to solve,\nthe format of the equation being [a^2 - b^2]\n")

a = float(input("\nEnter the value of a: "))
b = float(input("Enter the value of b: "))

difference_of_squares = (a**2) - (b**2)

print("\n\nThe difference of squares is " + str(difference_of_squares))